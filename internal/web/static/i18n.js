let LANG = localStorage.getItem("poxy_lang") ||
  (navigator.language || "it").slice(0, 2);
if (!I18N[LANG]) LANG = "it";

function t(key, vars) {
  let s = (I18N[LANG] && I18N[LANG][key]) || I18N.it[key] || key;
  if (vars) for (const k in vars) s = s.replace("{" + k + "}", vars[k]);
  return s;
}

function applyI18n() {
  document.documentElement.lang = LANG;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
}

function initLangSelector(onChange) {
  const sel = document.getElementById("lang");
  if (!sel) return;
  sel.innerHTML = "";
  LANGS.forEach((l) => {
    const o = document.createElement("option");
    o.value = l.code;
    o.textContent = l.name;
    if (l.code === LANG) o.selected = true;
    sel.appendChild(o);
  });
  sel.addEventListener("change", () => {
    LANG = sel.value;
    localStorage.setItem("poxy_lang", LANG);
    applyI18n();
    if (onChange) onChange();
  });
}
